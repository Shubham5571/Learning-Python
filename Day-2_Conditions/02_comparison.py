a = 10
b = 5

print("1. ",a<b)   # Less than
print("2. ",a>b)   # Greater than 
print("3. ",a==b)   # Equal to
print("4. ",a!=b)   # Not equal to
print("5. ",a<=b)   # Less than or equal to  
print("6. ",a>=b)   # Greater than or equal to
print("7. ",a is b)   # Identity operator
print("8. ",a is not b)   # Not identity operator
# Note: 'in' and 'not in' operators are generally used with sequences like lists, tuples, or strings.

